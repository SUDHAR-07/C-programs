#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main()
{  /* float and lf */
    printf("%f \n",pow(2,3));
    printf("%lf \n",pow(3,3));
    printf("%lf \n",sqrt(36));
    printf("%f \n",sqrt(40));
    printf("%f \n",ceil(40.66));
    printf("%f \n",floor(40.777777));
    return 0;
    
}